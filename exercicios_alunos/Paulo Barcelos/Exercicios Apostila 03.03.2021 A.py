##Escreva um código em Python para receber uma frase do usuário e transformar todos os
##caracteres em maiúsculo, minúsculo e capitalizado;

frase = input('Digite sua frase aqui: ')
print('Sua frase em maiusculo: ')
print(frase.upper())
print('Sua frase em minusculo: ')
print(frase.lower())
print('Sua frase captalizado: ')
print(frase.capitalize())
